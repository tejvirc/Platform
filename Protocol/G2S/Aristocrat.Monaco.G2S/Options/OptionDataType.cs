namespace Aristocrat.Monaco.G2S.Options
{
    /// <summary>
    ///     Available option data types for values.
    /// </summary>
    public enum OptionDataType
    {
        /// <summary>
        ///     The string data type.
        /// </summary>
        String = 0,

        /// <summary>
        ///     The integer data type.
        /// </summary>
        Integer = 1,

        /// <summary>
        ///     The boolean data type.
        /// </summary>
        Boolean = 2,

        /// <summary>
        ///     The decimal data type.
        /// </summary>
        Decimal = 3,

        /// <summary>
        ///     The complex data type.
        /// </summary>
        Complex = 4
    }
}